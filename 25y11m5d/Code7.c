/*描述
输出m的阶乘末尾有多少个0？（注意m阶乘的结果可能超出数值型数据的存储范围）
输入
第1行是一个整数n，表示随后有n组数据。每组输入数据占一行。
输出
对每组数据m，输出其阶乘末尾0的个数，并换行。
样例
输入
2
5
100
输出
1
24*/
#include <stdio.h>
int main(){
    int n;
    int m, a;
    scanf("%d", &n);
    for (int i = 0; i < n; i++) {
        scanf("%d", &m);
        a = m / 5 + m / 25 + m / 125 + m / 625 + m / 3125;
        printf("%d", a);
    }
}